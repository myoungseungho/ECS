// 같은 맵이라도 채널이 다르면 서로 보이지 않는다.
// 채널은 서버 부하 분산의 기본 단위.
//
// 예:
//   A(채널1, 셀0,0) ↔ B(채널1, 셀0,0) → 서로 보임 ✓
//   A(채널1, 셀0,0) ↔ C(채널2, 셀0,0) → 안 보임 ✗ (같은 좌표여도!)
//
// 실제 게임에서 채널의 역할:
//   - 서버 부하 분산: 한 맵에 3,000명 → 채널 3개로 분산 (1,000명씩)
//   - 유저 선택: "사냥터가 붐빈다" → 한가한 채널로 이동
//   - 이벤트 격리: 공성전 채널을 별도 운영

struct ChannelComponent {
    int channel_id = 0;  // 0 = 미배정
};
